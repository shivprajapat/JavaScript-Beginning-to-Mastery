document.write("<h3>Assignment Operators</h3>");

// Addition (+=)
// Subtraction (-=)
// Multiplication (*=)
// Exponentiation (**=)
// Division (/=)
// Modulus (%=)
// Simple Assign (=)

var a = 2;
var b = 3;

// Addition (+=)
 a+=b;
console.log("Addition (+=) =>", a);

// Subtraction (-=)
var c = 3;
var d = 2;
c-=d;
console.log("Subtraction (-=) =>", c);

// // Multiplication
var e = 3;
var f = 2;

e*=f;
console.log("Multiplication =>", e);

// Exponentiation
var g = 3;
var h = 2;

g **=h;
console.log("Exponentiation =>", g);

// Division
var i = 3;
var j = 2;

i /= j;
console.log("Division =>", i);

// Modulus
var k = 3;
var l = 2;

k %= l;
console.log("Modulus =>", k);

// Simple Assignment
var o = 3;
var p = 2;
o=p
console.log("Decrement =>", p);
