document.write("<h3>Comparison Operators</h3>");

// ==
// ===
// !=
// !==
// >
// <
// >=
// <=

// ==
var a = 2;
var b = "2";
console.log("(==) =>", a == b);

// ===
var c = 2;
var d = 2;
console.log("(===) =>", c === d);

// !=
var e = 3;
var f = 2;
console.log("(!=) =>", e != f);

// !==
var g = 2;
var h = "2";

console.log("(!==) =>", g !== h);

// >
var i = 3;
var j = 2;

console.log("(>) =>", i > j);

// <
var k = 2;
var l = 3;

console.log("(<) =>", k < l);

// >=
var m = 2;
var n = 1;

console.log("(>=) =>", m >= n);

// <=
var o = 5;
var p = 4;

console.log("(<=) =>", o <= p);
