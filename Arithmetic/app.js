document.write("<h3>Arithmetic Operators</h3>");

// Addition
// Subtraction
// Multiplication
// Exponentiation
// Division
// Modulus
// Increment
// Decrement

var a = 2;
var b = 3;

// Addition
var c = a + b;
console.log("Addition =>", c);

// Subtraction
var d = a - b;
console.log("Subtraction =>", d);

// Multiplication
var e = a * b;
console.log("Multiplication =>", e);

// Exponentiation
var f = a ** b;
console.log("Exponentiation =>", f);

// Division
var g = a / b;
console.log("Division =>", g);

// Modulus
var l = a % b;
console.log("Modulus =>", l);

// Increment
var m = a + b;
m++;
console.log("Increment =>", m);

// Decrement
var n = a - b;
n--;
console.log("Decrement =>", n);
